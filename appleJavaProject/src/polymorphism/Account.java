package polymorphism;

public class Account {
	
	private int id,number,balance;
	
	public void setId(int id)
	{
		this.id=id;
	}
	int getId()
	{
		return id;
	}
	
	public void setNumber(int number)
	{
		this.number=number;
	}
	int getNumber()
	{
		return number;
	}
	
	public void setBalance(int balance)
	{
		this.balance=balance;
	}
	int getBalance()
	{
		return balance;
	}

	
}

